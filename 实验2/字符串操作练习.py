a = "Hello"  # 定义变量a，b
b = "Python"
print("a + b 输出结果：", a + b)  # 连接a与b
print("a * 2 输出结果是：", a * 2)  # 将a重复打印两遍
print("a[1] 输出结果是：", a[1])  # 打印a的第二个字符
print("a[1:4] 输出结果是：", a[1:4])  # 打印a的第二个字符到第四个字符
if 'H' in a:  # 判断H是否在a中
    print("H 在变量 a 中")
else:
    print("H 不在变量 a 中")
if 'M' not in a:  # 判断M是否在a中
    print("M 不在变量 a 中")
else:
    print("M 在变量 a 中")
print(r'\n')
